namespace Wpf.Ui.Violeta.Controls;

public enum TimelineDisplayMode
{
    Left,
    Center,
    Right,
    Alternate,
}

/// <summary>
/// Placement of timeline item content relative to the icon/axis line.
/// Left means content is placed to the left of the line.
/// Right means content is placed to the right of the line.
/// Separate means line is placed between content and time.
/// </summary>
public enum TimelineItemPosition
{
    Left,
    Right,
    Separate,
}
